#!/usr/bin/python3
# This script prints a float number with a precision of 2 decimal places.
# It uses an f-string for formatting and avoids casting to string.

# The 'number' variable is provided as part of the task's source code.
# For this task, we'll use a sample float value.
number = 3.14159

# Print the float with 2 decimal places using f-string formatting.
# The ':.2f' inside the f-string formats the float to two decimal places.
print(f"Float: {number:.2f}")

